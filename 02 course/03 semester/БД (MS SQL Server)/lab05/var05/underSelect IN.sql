select Фамилия, Имя, Отчество from СТУДЕНТЫ
where ID_студента in (
    select ID_студента
    from ОЦЕНКИ
    where ID_предмета = (select ID_предмета from ПРЕДМЕТЫ where Название = 'Физика'));


select Фамилия, Имя, Отчество from СТУДЕНТЫ 
	join ОЦЕНКИ on СТУДЕНТЫ.ID_студента = ОЦЕНКИ.ID_студента
	join ПРЕДМЕТЫ on ОЦЕНКИ.ID_предмета = ПРЕДМЕТЫ.ID_предмета
	where ПРЕДМЕТЫ.Название = 'Физика'